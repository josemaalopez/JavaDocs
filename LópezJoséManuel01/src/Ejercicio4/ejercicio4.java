public class ejercicio4 {

	public static void main(String[] args) {
		
		byte numHijos = 3;
		short añoNacimiento= 1975;
		int poblacion = 49315949;
		char inicialNombre = 'J';
		double precioProducto = 499.99;
		float temperatura = 37.5f;
		String nombre = "José Manuel";
		
		System.out.println("El valor de la variable de tipo byte (numHijos) es: " + numHijos);
		System.out.println("El valor de la variable de tipo short (añoNacimiento) es: " + añoNacimiento);
		System.out.println("El valor de la variable de tipo int (población) es: " + poblacion);
		System.out.println("El valor de la variable de tipo char (inicialNombre) es: " + inicialNombre);
		System.out.println("El valor de la variable de tipo double (precioProducto) es: " + precioProducto + " \"€\"");
		System.out.println("El valor de la variable de tipo float (temperatura) es: " + temperatura);
		System.out.println("El valor de la variable de tipo String (nombre) es: " + nombre);
	}

}
